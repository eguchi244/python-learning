# 【if文 - 条件による分岐】
### if文の書き方
"""
if 条件式:
    # 条件式がTrueの場合に実行されるコード
"""
### else文の書き方
"""
if 条件式:
    # 条件式がTrueの場合に実行されるコード
else:
    # 条件式がFalseの場合に実行されるコード
"""
### elif文の書き方
"""
if 条件式1:
    # 条件式1がTrueの場合に実行されるコード
elif 条件式2:
    # 条件式1がFalseかつ条件式2がTrueの場合に実行されるコード
elif 条件式3:
    # 条件式1と条件式2がFalseかつ条件式3がTrueの場合に実行されるコード
else:
    # どの条件式にも当てはまらない場合に実行されるコード
"""

# 【for文 - 繰り返し処理】
"""
for 変数 in イテラブル:
    処理
"""

# 【while文 - 条件が満たされるまで繰り返し】
"""
while 条件式:
    処理
"""

# 【match文 - パターンマッチング】
"""
match 対象:
    case パターン1:
        # パターン1にマッチした場合の処理
    case パターン2:
        # パターン2にマッチした場合の処理
    case _:
        # どのパターンにもマッチしなかった場合の処理
"""
